#include "rush02.h"

// 1000000000000000000000000000000000000 is 37 length
char	*search_big(char *dict, size_t len)
{
	char	s[38];
	size_t	i;

	s[0] = '1';
	i = 1;
	while (i < len)
	{
		s[i] = '0';
		i++;
	}
	s[i] = '\0';
	return (search(dict, s));
}
